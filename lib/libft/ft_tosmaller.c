#include "libft.h"

int		*ft_tosmaller(int *n, size_t len)
{
	size_t	i;
	int		x;

	i = 0;
	while (i < --len)
	{
		while (i < len)
		{
			if (n[i] < n[i + 1])
			{
				x = n[i];
				n[i] = n[i + 1];
				n[i + 1] = x;
			}
			i++;
		}
		i = 0;
	}
	return (n);
}
